async function seedItems() {
  console.log('📦 Seeding ToxicityV2Item...');

  // 1) drug_single: D 배열 104개
  for (const item of D) {
    await prisma.toxicityV2Item.upsert({
      where: { mode_itemId: { mode: 'drug_single', itemId: item.id } },
      update: {},
      create: {
        mode: 'drug_single', itemId: item.id, num: item.num, name: item.n, category: item.c,
        species: item.s, duration: item.d, description: item.desc,
        priceOral: item.op, routeOral: item.or_, weeksOral: String(item.ow),
        priceIv: item.ip, routeIv: item.ir, weeksIv: String(item.iw),
        formalName: FN[item.id] ?? null, guideline: GL[item.id] ?? null,
      },
    });
  }
  console.log(`  ✓ drug_single: ${D.length} items`);

  // 2) drug_combo: COMBO 9개
  for (const item of COMBO) {
    await prisma.toxicityV2Item.upsert({
      where: { mode_itemId: { mode: 'drug_combo', itemId: item.id } },
      update: {},
      create: {
        mode: 'drug_combo', itemId: item.id, num: item.num, name: item.n, category: item.c,
        species: item.s, duration: item.d, description: item.desc,
        priceP2: item.p2, priceP3: item.p3, priceP4: item.p4,
        formalName: item.fn, guideline: item.gl, note: item.note ?? null,
        weeksOral: String(item.w),
      },
    });
  }
  console.log(`  ✓ drug_combo: ${COMBO.length} items`);

  // Helper for simple items
  async function seedSimple(mode: string, items: SimpleSeed[]) {
    for (const item of items) {
      await prisma.toxicityV2Item.upsert({
        where: { mode_itemId: { mode, itemId: item.id } },
        update: {},
        create: {
          mode, itemId: item.id, num: item.num, name: item.n, category: item.c,
          species: item.s, duration: item.d, description: item.desc,
          priceSingle: item.p, formalName: item.fn, guideline: item.gl,
          note: item.note ?? null, weeksOral: String(item.w),
        },
      });
    }
    console.log(`  ✓ ${mode}: ${items.length} items`);
  }

  // 3~9) 나머지 모드
  await seedSimple('drug_vaccine', VACCINE);
  await seedSimple('drug_screen_tox', SCREEN);
  await seedSimple('drug_screen_cv', CV_SCREEN);
  await seedSimple('hf_indv', HF_INDV);
  await seedSimple('hf_prob', HF_PROB);
  await seedSimple('hf_temp', HF_TEMP);
  await seedSimple('md_bio', MD_BIO);
}

